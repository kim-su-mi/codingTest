public class CellPhone {
	String model; // 모델명
	String number; // 전화번호
	int chord; // 화음

	public void setNumber(String n) {
		number = n;
	}

	public String getModel() {
		return model;
	}

	public int getChord() {
		return chord;
	}

	public String getNumber() {
		return number;
	}

}
